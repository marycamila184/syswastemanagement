    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy"); 
        Date date = new Date(); 
        String undMensal=campoUndMensal.getText();
        String qtdeT=campoQtde.getText();
        int qtde= Integer.parseInt(qtdeT);
        String descricao=campoDescricao.getText();
        String dataInclusao=dateFormat.format(date);
        pi.insertServicos(undMensal, qtde, descricao, dataInclusao);
        campoUndMensal.setText("");
        campoQtde.setText("");
        campoDescricao.setText("");
        JOptionPane.showMessageDialog(null, "O cadastro foi efetuado com sucesso!");        
        dispose();
    }//GEN-LAST:event_botaoSalvarActionPerformed

